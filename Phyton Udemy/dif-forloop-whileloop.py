# Diferença entre IF/ELSE, FOR LOOP e WHILE LOOP... 

# IF/ELSE sempre executa apenas 1 vez = Basicamente verdadeiro e falso
# FOR LOOP define quantas vezes a variavel vai girar dentro do codigo = Utilizado quando não se sabe o tamanho do seu loop
# WHILE LOOP após receber o comando, caso a variavel for isso ou aquilo ela gira uma certa quantidade = Utilizado quando se tem um condição

