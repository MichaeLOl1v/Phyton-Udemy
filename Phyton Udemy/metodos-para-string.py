# Ao inserir a variavel no print colocar '.' após o nome para variavel para selecionar o metodo. Exemplo print(mensagem.)

mensagem = 'eu gosto de comida caseira'

#Imprimir tudo em letra minuscula 'lower'
print(mensagem.lower()) 

#Imprimir tudo em letra maiuscula 'upper'
print(mensagem.upper()) 

#Imprimir a primeira letra maiuscula 'capitalize'
print(mensagem.capitalize())

#Procura uma letra/palavra (posição) em especifico 'find'
print(mensagem.find('c'))

#Oque eu quero encontrar e para oque quer trocar 'replace'
print(mensagem.replace('a', 'e'))
#No exemplo acima foi trocada a letra A pela letra E na frase (Funciona para letras e palavras)

#Remove qualquer espaço antes do primeiro caractere 'strip'
print(mensagem.strip())

